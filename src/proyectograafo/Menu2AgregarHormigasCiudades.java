    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                if(Integer.parseInt(numerodeCiudades.getText())!=0){
           String dato= Entrada.getText();
        grafo.nuevaCiudad(dato);
        Salida.setText(grafo.toString());
        Entrada.setText("");
           int numeroCiudadesactualizado=Integer.parseInt(numerodeCiudades.getText())-1;
           numerodeCiudades.setText(Integer.toString(numeroCiudadesactualizado));
        }else{
            warningCiudades=new JFrame();
            JOptionPane.showMessageDialog(warningCiudades, "Ya has introducido todas las ciudades necesarias!", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String origen = "";
        String destino = "";
        float distancia;
        origen = JOptionPane.showInputDialog("Origen");
        destino = JOptionPane.showInputDialog("Destino");
        distancia = Float.parseFloat(JOptionPane.showInputDialog("Distancia"));
        if(grafo.existeCiudad(origen) && grafo.existeCiudad(destino)){
            grafo.nuevoCamino(origen, destino, distancia);
        }
        Salida.setText(grafo.toString());
    }//GEN-LAST:event_jButton2ActionPerformed

    
